function getTranslationKey(value: string): string {
  return value.trim().replace(/\s+/g, ' ');
}

export function translateUiText(
  value: string,
  language: SupportedLanguage,
): string {
  if (language === 'en' || !value.trim()) {
    return value;
  }

  const targetLanguage = language;
  const leading = value.match(/^\s*/)?.[0] ?? '';
  const trailing = value.match(/\s*$/)?.[0] ?? '';
  const key = getTranslationKey(value);
  const translated =
    UI_TRANSLATION_OVERRIDES[key]?.[targetLanguage] ??
    UI_TRANSLATIONS[key]?.[targetLanguage];

  if (!translated) {
    return value;
  }

  return `${leading}${translated}${trailing}`;
}

export function hasUiTranslation(
  value: string,
  language: SupportedLanguage,
): boolean {
  if (language === 'en') {
    return false;
  }

  const key = getTranslationKey(value);
  return Boolean(
    UI_TRANSLATION_OVERRIDES[key]?.[language] ?? UI_TRANSLATIONS[key]?.[language],
  );
}
